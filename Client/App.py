if __name__ == "__main__":
    """
    Entry point of application.
    """
    app = MyApp()
    loop = asyncio.new_event_loop()
    asyncio.set_event_loop(loop)
    threading.Thread(target=lambda: loop.run_until_complete(update())).start()
    app.run()
